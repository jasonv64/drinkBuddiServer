DROP IF EXISTS drinkbuddidb;
CREATE DATABASE drinkbuddidb;
USE drinkbuddidb;

DROP TABLE IF EXISTS `USERS`;
CREATE TABLE `USERS` (
    `USER_ID` varchar(64) NOT NULL,
    `FIRST_NAME` varchar(64) DEFAULT NULL,
    `LAST_NAME` varchar(64) DEFAULT NULL,
    `EMAIL` varchar(64) DEFAULT NULL,
    `STATE` varchar(64) DEFAULT NULL,
    `ZIP` varchar(64) DEFAULT NULL,
    `PASSWORD` varchar(64) DEFAULT NULL,
    `SUBSCRIPTION_ID` varchar(64) DEFAULT NULL,
    `PICTURE` varchar(255) DEFAULT NULL,
    `DATE_JOINED` datetime DEFAULT NULL,
    PRIMARY KEY(`USER_ID`)
);

DROP TABLE IF EXISTS `SUBSCRIPTION`;
CREATE TABLE `SUBSCRIPTION` (
    `SUBSCRIPTION_ID` varchar(64) NOT NULL,
    `MONTHLY_PRICE` varchar(64) NOT NULL,
    `YEARLY_PRICE` varchar(64) NOT NULL,
    `DESCRIPTION` varchar(64) NOT NULL,
    PRIMARY KEY(`SUBSCRIPTION_ID`)
);

DROP TABLE IF EXISTS `VENDORS`;
CREATE TABLE `VENDORS` (
    `VENDOR_ID` varchar(64) NOT NULL,
    `VENDOR` varchar(64) NOT NULL,
    `DESCRIPTION` varchar(64) NOT NULL,
    `PRIMARY_CONTACT` varchar(64) NULL,
    `EMAIL` varchar(64) NULL,
    `PHONE` varchar(64) NULL,
    `STREET` varchar(64) NULL,
    `CITY` varchar(64) NULL,
    `ZIP` varchar(255) NULL,
    `STATE` varchar(64) NULL,
    `OFFER_ID` varchar(64) NULL,
    `MENU_ID` varchar(64) NULL,
    PRIMARY KEY(`VENDOR_ID`)
);

DROP TABLE IF EXISTS `VENDOROFFERS`;
CREATE TABLE `VENDOROFFERS` (
    `OFFER_ID` varchar(64) NOT NULL,
    `DESCRIPTION` varchar(64) NOT NULL,
    `IMAGE` varchar(64) NOT NULL,
    PRIMARY KEY(`OFFER_ID`)
);

DROP TABLE IF EXISTS `VENDORMENU`;
CREATE TABLE `VENDOROFFERS` (
    `MENU_ITEM_ID` varchar(64) NOT NULL,
    `MENU_ID` varchar(64) NOT NULL,
    `DESCRIPTION` varchar(64) NOT NULL,
    `IMAGE` varchar(64) NOT NULL,
    PRIMARY KEY(`MENU_ITEM_ID`)
);
